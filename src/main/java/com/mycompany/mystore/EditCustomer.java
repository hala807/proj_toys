    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // Get all customer information
            String custID = jTextField1.getText().trim();
            String custFName = jTextField2.getText().trim();
            String custLName = jTextField4.getText().trim();
            String custphone = jTextField5.getText().trim();
            String custEmail = jTextField6.getText().trim();
            String custCity = jTextField7.getText().trim();
            
            // Validate required fields
            if (custID.isEmpty() || custFName.isEmpty() || custLName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Customer ID, First Name, and Last Name are required fields", "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Connect to database
            DriverManager.registerDriver(new org.postgresql.Driver());
            String url = "jdbc:postgresql://localhost:5432/postgres";
            String username = "sami";
            String password = "123456";
            
            Connection con = DriverManager.getConnection(url, username, password);
            con.setAutoCommit(false);
            Statement stmt = con.createStatement();
            
            // First check if customer exists
            String checkQuery = "SELECT * FROM customer WHERE c_id = '" + custID + "'";
            ResultSet rs = stmt.executeQuery(checkQuery);
            
            if (rs.next()) {
                // Customer exists, perform UPDATE
                String updateQuery = "UPDATE customer SET " +"first_name = '" + custFName + "', " + "last_name = '" + custLName + "', " +
                    "phone_number = '" + custphone + "', " +
                    "email = '" + custEmail + "', " +
                    "city = '" + custCity + "' " +
                    "WHERE c_id = '" + custID + "'";
                stmt.executeUpdate(updateQuery);
                JOptionPane.showMessageDialog(this, "Customer information updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            } 
            
            // Commit transaction and close connections
            con.commit();
            rs.close();
            stmt.close();
            con.close();
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

