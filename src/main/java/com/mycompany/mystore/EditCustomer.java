    private void jButtonSearchCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchCustomerActionPerformed
        try {
            String customerId = jTextField1.getText().trim();
            
            if (customerId.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Customer ID", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Connect to database
            DriverManager.registerDriver(new org.postgresql.Driver());
            String url = "jdbc:postgresql://localhost:5432/postgres";
            String username = "sami";
            String password = "123456";
            
            Connection con = DriverManager.getConnection(url, username, password);
            Statement stmt = con.createStatement();
            
            // Query to get customer information
            String query = "SELECT * FROM customer WHERE c_id = '" + customerId + "'";
            ResultSet rs = stmt.executeQuery(query);
            
            if (rs.next()) {
                // Populate fields with customer information
                jTextField2.setText(rs.getString("first_name"));
                jTextField4.setText(rs.getString("last_name"));
                jTextField5.setText(rs.getString("phone_number"));
                jTextField6.setText(rs.getString("email"));
                jTextField7.setText(rs.getString("city"));
            } else {
                JOptionPane.showMessageDialog(this, "Customer not found", "Search Result", JOptionPane.INFORMATION_MESSAGE);
                // Clear all fields
                jTextField2.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
            }
            
            // Close connections
            rs.close();
            stmt.close();
            con.close();
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSearchCustomerActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        String customerId = jTextField1.getText().trim();
        
        if (customerId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please search for a customer first", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete this customer?", 
            "Confirm Delete", 
            JOptionPane.YES_NO_OPTION);
            
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // Connect to database
                DriverManager.registerDriver(new org.postgresql.Driver());
                String url = "jdbc:postgresql://localhost:5432/postgres";
                String username = "sami";
                String password = "123456";
                
                Connection con = DriverManager.getConnection(url, username, password);
                Statement stmt = con.createStatement();
                
                // Delete the customer
                String query = "DELETE FROM customer WHERE c_id = '" + customerId + "'";
                int result = stmt.executeUpdate(query);
                
                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Customer deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    // Clear all fields
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField7.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Customer not found", "Error", JOptionPane.ERROR_MESSAGE);
                }
                
                // Close connections
                stmt.close();
                con.close();
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

